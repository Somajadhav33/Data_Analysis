#Integer
num1 = 20
print(num1.bit_length()) #count bit length 
print(bin(num1)) #Convert to binary
print(hex(num1)) #Convert to hexadecimal
print(oct(num1)) #Convert to octal


#Float 
float_num = 3.1414
print(float_num)

#Complex
complex_num = 3j + 3
print(complex_num)

#Built-in Math Function
num2 = -11
print(abs(num2))

num3 = 92.02930291
print(round(num3,4))

num4 = 4
